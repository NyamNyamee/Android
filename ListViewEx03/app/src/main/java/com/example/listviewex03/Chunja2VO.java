package com.example.listviewex03;

public class Chunja2VO {
    private int idx;
    private String h;
    private String k;
    private String t;

    public int getIdx() {
        return idx;
    }

    public void setIdx(int idx) {
        this.idx = idx;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    @Override
    public String toString() {
        return "Chunja2VO{" +
                "idx=" + idx +
                ", h='" + h + '\'' +
                ", k='" + k + '\'' +
                ", t='" + t + '\'' +
                '}';
    }
}
